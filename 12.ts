type Direction = "up" | "left" | "down" | "right";
type Position = { x: number; y: number };

const matrix = input.split("\n").map((row) => row.split(""));

const guard = findGuard(matrix);

const getNextPosition = (props: { direction: Direction; position: Position }) =>
  toNextPosition({
    ...props,
    maxY: matrix.length - 1,
    maxX: matrix[0].length - 1,
  });

let obstaclesCount = 0;

for (let y = 0; y < matrix.length; y++) {
  for (let x = 0; x < matrix[y].length; x++) {
    let position = guard;
    let direction: Direction = "up";
    const path = new Map<string, null>();

    do {
      if (position == null) break;

      const nextPosition = getNextPosition({ direction, position });
      if (nextPosition == null) break;

      const current = `${position.x}:${position.y}:${direction}`;
      if (path.get(current) === null) {
        obstaclesCount++;
        break;
      }
      path.set(current, null);

      if (
        matrix[nextPosition.y][nextPosition.x] === "#" ||
        (nextPosition.x === x && nextPosition.y === y)
      ) {
        direction = changeDirection(direction);
        continue;
      }

      position = nextPosition;
    } while (true);
  }
}

function toNextPosition(props: {
  direction: Direction;
  position: Position;
  maxY: number;
  maxX: number;
}): Position | null {
  const { x, y } = props.position;
  if (props.direction === "up") {
    const nextPosition = { x, y: y - 1 };

    if (nextPosition.y < 0) return null;

    return nextPosition;
  }
  if (props.direction === "left") {
    const nextPosition = { x: x - 1, y };

    if (nextPosition.x < 0) return null;

    return nextPosition;
  }
  if (props.direction === "down") {
    const nextPosition = { x, y: y + 1 };

    if (nextPosition.y > props.maxY) return null;

    return nextPosition;
  }
  if (props.direction === "right") {
    const nextPosition = { x: x + 1, y };

    if (nextPosition.x > props.maxX) return null;

    return nextPosition;
  }

  throw Error("Unreachable code.");
}

function changeDirection(direction: Direction): Direction {
  if (direction === "up") return "right";
  if (direction === "right") return "down";
  if (direction === "down") return "left";
  if (direction === "left") return "up";

  throw Error("Unreachable code.");
}

function findGuard(matrix: string[][]): Position | null {
  for (let y = 0; y < matrix.length; y++) {
    for (let x = 0; x < matrix[y].length; x++) {
      if (matrix[y][x] === "^") return { x, y };
    }
  }

  return null;
}

