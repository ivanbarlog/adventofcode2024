function toMatrix(input: string): string[][] {
  return input.split("\n").map((row) => row.split(""));
}

// ⬆️
function north(matrix: string[][]): string {
  const columns = new Array<Array<string>>();

  for (let i = 0; i < matrix.length; i++) {
    const row = matrix[i];
    const column = new Array<string>();
    for (let j = 0; j < row.length; j++) {
      column.push(matrix[i][j]);
    }
    columns.push(column);
  }

  return columns[0]
    .map((_, rowIndex) =>
      columns
        .map((column) => column[rowIndex])
        .reverse()
        .join("")
    )
    .join("\n");
}

// ↗️
function northEast(matrix: string[][]): string {
  const rows = new Array<Array<string>>();

  for (let i = 0; i < matrix.length; i++) {
    const row = matrix[i];
    for (let j = 0; j < row.length; j++) {
      const rowIndex = i + j;
      if (rows[rowIndex] == null) rows[rowIndex] = new Array<string>();

      rows[rowIndex].push(matrix[i][j]);
    }
  }

  return rows.map((row) => row.reverse().join("")).join("\n");
}

