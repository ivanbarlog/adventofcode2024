function southEast(matrix: string[][]): string {
  const rows = new Array<Array<string>>();

  for (let i = 0; i < matrix.length; i++) {
    const row = matrix[i];
    for (let j = 0; j < row.length; j++) {
      const rowIndex = row.length - 1 - (j - i);
      if (rows[rowIndex] == null) rows[rowIndex] = new Array<string>();

      rows[rowIndex].push(matrix[i][j]);
    }
  }

  return rows.map((row) => row.join("")).join("\n");
}

